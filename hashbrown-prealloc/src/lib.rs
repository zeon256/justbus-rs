    pub fn with_ttl(ttl: Duration) -> Self {
        let mut map = HashMap::new();

        Self::BUS_STOP_ID.into_iter().for_each(|v| {
            map.insert(
                *v,
                InternalEntry::new(String::with_capacity(1024), Instant::now()),
            );
        });

        CachePreAlloc { map, ttl }
    }

    pub fn insert(&mut self, key: u32, value: &str) {
        let data = self.map.get_mut(&key);

        if let Some(v) = data {
            v.value.clear();
            v.value.push_str(value);
            v.expiration = Instant::now() + self.ttl;
        }
    }

    pub fn get(&self, key: u32) -> Option<&String> {
        self.map.get(&key).and_then(|f| f.get())
    }
